userChord = input()

if userChord=="G":
    print("e|-3-")
    print("B|-0-")
    print("G|-0-")
    print("D|-0-")
    print("A|-2-")
    print("E|-3-")

elif userChord=="C":
    print("e|-0-")
    print("B|-1-")
    print("G|-0-")
    print("D|-2-")
    print("A|-3-")
    print("E|---")

elif userChord=="D":
    print("e|-2-")
    print("B|-3-")
    print("G|-2-")
    print("D|-0-")
    print("A|---")
    print("E|---")

else:
    print(userChord+" is not a supported chord.")
